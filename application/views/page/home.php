<p>
    <h3>Toeval of Niet: rechtspraak</h3></p>
 <p>
     Op namenlijst.rechtspraak.nl worden nevenfuncties van diverse functionarissen in de rechtspraak geregistreerd. 
     Dit publieke register is enkel toegankelijk op deelniveau, dus als je weet wat je zoekt.</p>
 <p>Toeval of Niet: rechtspraak maakt deze informatie direct toegankelijk middels totaaloverzichten en stelt de data als open data ter beschikking.
    </p>
<h3>Open Data</h3>
<p>De gehele database in het oorspronkelijke formaat te downloaden. Het is een gecomprimeerd json bestand dat bestaat uit ongeveer 
 4500 hashes met daarin de velden: 'name', 'set' (De instantie), en 'html'. Het html veld bevat de gehele gecrawlde pagina plus de oorspronkelijke http headers.</p>

