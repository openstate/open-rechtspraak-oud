    <h3>Toeval of Niet: rechtspraak</h3></p>
 <p>
     Op namenlijst.rechtspraak.nl worden nevenfuncties van diverse functionarissen in de rechtspraak geregistreerd. Dit publieke register is enkel toegankelijk op deelniveau, dus als je weet wat je zoekt.</p>
 <p>Toeval of Niet: rechtspraak maakt deze informatie direct toegankelijk middels totaaloverzichten en stelt de data als open data ter beschikking.
